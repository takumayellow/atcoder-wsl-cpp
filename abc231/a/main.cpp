/**
 * @brief setw()とsetfill()による数値フォーマット関数の詳細解説
 * 
 * 【setw(int n) - Set Width】
 * - ヘッダ: <iomanip>
 * - 機能: 次に出力される値の**最小フィールド幅**を設定
 * - 引数: n = 出力幅（文字数）
 * - 動作:
 *   - 出力する値がn文字未満の場合、不足分を埋め文字で補填
 *   - 出力する値がn文字以上の場合、すべての桁を出力（切り捨てなし）
 * - 影響範囲: **次の1回の出力のみ**に適用される（非永続的）
 * 
 * 例:
 *   cout << setw(5) << 42;      // "   42" (5文字幅、デフォルトは空白埋め)
 *   cout << setw(3) << 12345;   // "12345" (幅を超えても全桁出力)
 *   cout << setw(3) << 7;       // "  7"   (3文字幅)
 *   cout << 99;                 // "99"    (setwの効果は1回限り)
 * 
 * 【setfill(char c) - Set Fill Character】
 * - ヘッダ: <iomanip>
 * - 機能: setwで幅を埋める際に使用する**埋め文字**を設定
 * - 引数: c = 埋め文字（通常は'0'または' '）
 * - デフォルト: 空白文字(' ')
 * - 影響範囲: **設定後すべての出力に適用**（永続的、再設定まで有効）
 * 
 * 例:
 *   cout << setfill('0') << setw(3) << 7;    // "007"
 *   cout << setfill('*') << setw(5) << 42;   // "***42"
 *   cout << setfill(' ') << setw(4) << 9;    // "   9" (デフォルトに戻す)
 * 
 * 【組み合わせ使用パターン】
 * 
 * 1. ゼロ埋め固定長出力（本コードの使用例）:
 *    cout << setw(3) << setfill('0') << n;
 *    - n=1   → "001"
 *    - n=42  → "042"
 *    - n=999 → "999"
 *    用途: ID、コンテスト番号、日付など
 * 
 * 2. 右寄せ（デフォルト）:
 *    cout << setw(5) << 42;  // "   42"
 * 
 * 3. 左寄せ:
 *    cout << left << setw(5) << 42;  // "42   "
 * 
 * 【本コードでの具体的動作】
 * 
 * cout << "AGC" << setw(3) << setfill('0') << n << endl;
 * 
 * ステップ1: "AGC"を出力
 * ステップ2: setfill('0')で埋め文字を'0'に設定
 * ステップ3: setw(3)で次の出力幅を3文字に設定
 * ステップ4: nを出力
 *   - n=5  の場合: "005" (1桁→ゼロ2つ追加)
 *   - n=42 の場合: "042" (2桁→ゼロ1つ追加)
 *   - n=100の場合: "100" (3桁→そのまま)
 * ステップ5: endlで改行
 * 
 * 結果例:
 *   n=5  → "AGC005"
 *   n=42 → "AGC043" (本問題ではn+1が出力される条件)
 * 
 * 【注意点】
 * - setwは次の1回のみ有効、setfillは変更するまで有効
 * - 推奨順序: setfill() → setw() → 値
 * - 順序が逆でも動作するが、可読性のため上記順序を推奨
 * - 数値以外（文字列など）にも使用可能
 * 
 * 【代替手段】
 * C++20以降:
 *   #include <format>
 *   cout << format("AGC{:03d}", n);  // より直感的
 * 
 * printf形式（C言語）:
 *   printf("AGC%03d\n", n);          // 同等の出力
 */
/**
 * @file main.cpp
 * @brief AtCoder ABC231 A問題の解答プログラム - AGCコンテスト番号変換
 * 
 * @details
 * このプログラムは、AtCoderのコンテスト番号付けルールに基づいて
 * AGCコンテストの番号を出力します。
 * 
 * 【問題の背景】
 * AGC（AtCoder Grand Contest）は特定の番号付けルールに従っており、
 * AGC042が欠番となっています。そのため、42以降の番号は1つずつずれます。
 * 例: 通常の42番目のコンテスト → AGC043として開催
 * 
 * 【アルゴリズム詳細】
 * 1. 標準入力から整数nを読み込む
 *    - nは何番目のAGCコンテストかを表す（1-indexed）
 * 2. nが42未満の場合：
 *    - そのままの番号を使用（AGC001, AGC002, ..., AGC041）
 *    - 理由: 欠番の影響を受けないため
 * 3. nが42以上の場合：
 *    - n+1の番号を使用（AGC043, AGC044, ...）
 *    - 理由: AGC042が欠番のため、1つ番号を繰り上げる
 * 4. "AGC"の後に3桁ゼロ埋めで番号を出力
 *    - 形式: AGCxxx（xxxは001-999の3桁数字）
 * 
 * 【使用技術の詳細解説】
 * 
 * ■ 高速入出力最適化:
 * - ios::sync_with_stdio(false):
 *   - C++のストリーム（cin/cout）とCの標準入出力（scanf/printf）の同期を無効化
 *   - 同期が無効化されると、cin/coutが独自のバッファを使用し高速化
 *   - 注意: この設定後はscanf/printfと混在使用不可
 *   - 競技プログラミングでは大量のデータ処理時に有効
 * 
 * - cin.tie(nullptr):
 *   - デフォルトでは、cinを使う前に自動的にcoutのバッファがフラッシュされる
 *   - この紐付け（tie）を解除することで、不要なフラッシュを防ぎ高速化
 *   - 注意: インタラクティブ問題では明示的なflushが必要になる場合がある
 * @see https://atcoder.jp/contests/abc231/tasks/abc231_a
 */
#include <bits/stdc++.h>
#include <atcoder/all>
using namespace std;
using namespace atcoder;
using ll = long long;

/**
 * @brief メイン関数
 * 
 * 標準入力から整数nを受け取り、以下の条件に従ってコンテスト番号を出力する：
 * - n < 42 の場合: "AGC" + 3桁ゼロ埋めのn を出力
 * - n >= 42 の場合: "AGC" + 3桁ゼロ埋めの(n+1) を出力
 * 
 * @return int プログラムの終了ステータス（常に0）
 * 
 * @note 高速入出力のため、sync_with_stdioをfalseに設定し、cin.tieをnullptrに設定している
 * @note setw(3)とsetfill('0')を使用して数値を3桁のゼロ埋め形式でフォーマットしている
 */
int main(){
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    
    int n;
    cin >> n;
    if (n < 42) {
        cout << "AGC" << setw(3) << setfill('0') << n << endl;
    } else {
        cout << "AGC" << setw(3) << setfill('0') << n + 1 << endl;
    }
    return 0;
}
